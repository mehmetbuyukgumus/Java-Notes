package introduction;
public class data_types {
    public static void main(String[] args) {
        int sayi = 12;
        sayi = 13;
        System.out.println(sayi);
        byte sayim = 127;
        sayim = -110;
        System.out.println(sayim);
        String name = "Mehmet";
        name = "Ahmet";
        System.out.println(name);
        long x = 10 , y = 12;
        System.out.println(x);
        System.out.println(y);
    }
    
}
